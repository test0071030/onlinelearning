function show_video(){
    var x = document.getElementById("middle_video_background");
    x.style.display = "block";
    var y = document.getElementById("video");
    y.style.display = "block";
};

function hide_video(){
    var x = document.getElementById("middle_video_background");
    x.style.display = "none";
    var y = document.getElementById("video");
    y.style.display = "none";
};